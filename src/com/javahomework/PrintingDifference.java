package com.javahomework;

public class PrintingDifference {

	public static void main(String[] args) {
		int a=20;
		int b=10;
		    a=a+b; //30
		    System.out.println(a);
		    a=a-b; //20
		    System.out.println(a);
		    b=a+b; // 30
		    System.out.println(b);
		    b=a-b; //-10
		    System.out.println(b);
		    

	}

}
